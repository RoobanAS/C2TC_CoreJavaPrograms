package Day1;

public class CharDemo {

	public static void main(String[] args) {
		
		char ch1 = 'a';
		System.out.println(ch1);

		char ch2 = 65;
		System.out.println(ch2);

		char uni1 = '\u00A7';
		System.out.println(uni1);

		char uni2 = '\u20AC';
		System.out.println(uni2);
		
		int ascii = 'A';
		System.out.println(ascii);

	}

}
